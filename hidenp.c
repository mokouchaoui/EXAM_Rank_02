{
    if (ac == 3)
	{
		while (*av[2])
		{
			if (*av[1] && *av[1] == *av[2])
				av[1]++;
			av[2]++;
		}
		if (*av[1] == '\0')
		{
			write (1, "1\n" ,2);
		}
	}
	if (*av[1])
		write(1 ,"0\n", 2);
	return (0);
     if (ac != 3)	 
	{
		write(1, "\n", 1);
		return (0);
	}
}
